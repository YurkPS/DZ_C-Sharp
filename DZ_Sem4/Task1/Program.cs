//Напишите программу, которая бесконечно запрашивает 
//целые числа с консоли. Программа завершается при 
//вводе символа ‘q’ или при вводе числа, сумма цифр 
//которого чётная.

using System;
class Program
{
    static void Main(string[] args)
    {
        while (true)
        {
            Console.WriteLine("Введите целое число или 'q' для завершения:");
            string input = Console.ReadLine();

            if (input == "q")
            {
                Console.WriteLine("Программа завершена.");
                break;
            }

            int number;
            if (!int.TryParse(input, out number))
            {
                Console.WriteLine("Неверный формат числа!");
                continue;
            }

            int sum = 0;
            foreach (char digit in number.ToString())
            {
                sum += int.Parse(digit.ToString());
            }

            if (sum % 2 == 0)
            {
                Console.WriteLine("Сумма цифр является чётным числом. Программа завершена.");
                break;
            }
        }
    }
}
//Программа будет запросить ввод целого числа или 
//символа 'q'. Если введено 'q', программа завершится. 
//Если введено целое число, программа проверит сумму 
//его цифр. Если сумма цифр является чётным числом, 
//программа также завершится. В противном случае программа
//будет запрашивать новое число снова.

//Обратите внимание, что программа проверяет только 
