//Задача 2: Задайте массив заполненный случайными 
//трёхзначными числами. Напишите программу, которая 
//покажет количество чётных чисел в массиве.

using System;

class Program
{
    static void Main()
    {
        Random random = new Random();
        int arraySize = 10; // размер массива, можно изменить

        int[] numbers = new int[arraySize];

        // Заполнение массива случайными трехзначными числами
        for (int i = 0; i < arraySize; i++)
        {
            numbers[i] = random.Next(100, 1000);
        }

        int evenCount = 0;

        // Подсчет количества четных чисел
        foreach (int number in numbers)
        {
            if (number % 2 == 0)
            {
                evenCount++;
            }
        }

        Console.WriteLine("Количество четных чисел в массиве: " + evenCount);
    }
}

//Программа создает массив указанного размера, заполняет 
//его случайными трехзначными числами и затем, с помощью 
//цикла, проверяет каждое число в массиве на четность. 
//Если число четное, счетчик evenCount увеличивается на 1. 
//В конце программа выводит количество четных чисел в 
//массиве на экран.